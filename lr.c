int lab3(){
int c; // текущий символ из потока
    int prev_c; // предыдущий символ
    int flag = NO; // признак слова
    int cnt = 0; // счётчик

    // цикл чтения символов из потока, связанного с клавиатурой
    while ((c = getchar()) != EOF) {
        if (c == 'q') {
            break; // остановка программы при вводе символа 'q'
        }
        if (c == ' ' ||  c == '.'  || c == '\n'  || c == ',') { // если символ является разделителем
            flag = NO; // сброс флага
            prev_c = ' '; // сброс предыдущего символа
        }
        else // иначе если символ не является разделителем
        {
            if (flag == NO){ // если программа не в слове
                flag = YES; // программа находится в слове
                prev_c = c; // присваивает прыдущему символу нынешний
            }

            else // иначе если программа в слове
            {
                if(prev_c == c) // если пред. символ равен нынешнему
                {
                    cnt++; // увеличить счетчик
                    prev_c = ' '; // сброс предыдущего символа
                }
                else // если предыдущий символ не равен предыдущему
                {
                    prev_c = ' '; // сброс предыдущего символа
                }
            }
        }
    }

    printf("Number of words = %d\n", cnt);
    return 0;
}

int lab4(){
char str[1000]; // Массив для хранения введенной строки
    printf("Enter line: "); // Вывод приглашения для ввода строки
    fgets(str, 1000, stdin); // Чтение строки из стандартного ввода

    int i = 0, j = 0; // Инициализация счетчиков
    while (str[i] != '\0') // Пока не достигнут конец строки
    {
        // Если текущий символ является первым символом строки или предыдущий символ - пробел, и это гласная
        if ((i == 0 || str[i - 1] == ' ') && line(str[i]))
        {
            // Пропускаем все символы до пробела или конца строки
            while (str[i] != ' ' && str[i] != '\0')
            {
                i++;
            }
        } else
        {
            // В противном случае, копируем символ в выходную строку
            str[j++] = str[i++];
        }
    }
    str[j] = '\0'; // Добавляем нулевой символ в конце строки

    printf("Result: %s\n", str); // Выводим результат

    return 0;
}

