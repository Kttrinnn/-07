int lab6(){
    int N=3, K=4; // Объявление переменных для размера массива
    int array[N][K]; // Объявление массива с заданными размерами
    for (int i = 0; i < N; i++) { // Цикл для перебора строк массива
        for (int j = 0; j < K; j++) { // Вложенный цикл для перебора столбцов массива
            scanf("%d", &array[i][j]); // Считывание элемента массива
        }
    }

    int min_element = array[0][0]; // Инициализация минимального элемента значением первого элемента массива
    int min_row = 0; // Инициализация индекса строки, содержащей минимальный элемент

    for (int i = 0; i < N; i++) { // Цикл для перебора строк массива
        for (int j = 0; j < K; j++) { // Вложенный цикл для перебора столбцов массива
            if (array[i][j] < min_element) { // Проверка, является ли текущий элемент минимальным
                min_element = array[i][j]; // Обновление значения минимального элемента
                min_row = i; // Обновление индекса строки с минимальным элементом
            }
        }
    }

    for (int i = min_row + 1; i < N; i++) { // Цикл для обнуления элементов строк после строки с минимальным элементом
        for (int j = 0; j < K; j++) { // Вложенный цикл для перебора столбцов массива
            array[i][j] = 0; // Обнуление элемента массива
        }
    }

    printf("The modified array is: \n"); // Вывод заголовка для модифицированного массива
    for (int i = 0; i < N; i++) { // Цикл для вывода модифицированного массива
        for (int j = 0; j < K; j++) { // Вложенный цикл для перебора столбцов массива
            printf("%d ", array[i][j]); // Вывод элемента массива
        }
        printf("\n"); // Переход на новую строку после каждой строки массива
    }

    return 0;
}

int lab7(){
unsigned int N;
    scanf("%u", &N);
    unsigned int swap, nec, chet;
    // Маска для извлечения нечетных битов
    unsigned int mask1 = 0xAAAAAAAA;
    // Маска для извлечения четных битов
    unsigned int mask2 = 0x55555555;
    // Сдвигаем нечетные биты вправо и четные влево на один бит
    nec = (N & mask1) >> 1;
    chet = (N & mask2) << 1;
    // Комбинируем сдвинутые биты
    swap = nec | chet;
    printf("%u", swap);
    return 0;
}
